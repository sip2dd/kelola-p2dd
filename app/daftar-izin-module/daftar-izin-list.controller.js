(function() {
    'use strict';

    angular
        .module('daftar-izin-module')
        .controller('DaftarIzinListController', DaftarIzinListController);

    /* @ngInject */
    function DaftarIzinListController(
        $http, $state, EnvironmentConfig, ElementConfig, $scope, $timeout, $q,
        $document, $mdDialog, $mdMedia, DaftarIzinFactory
    ) {
        var vm = this;

        vm.showFormView = function(id) {
            $state.go('triangular.admin-default.daftar-izin-view', { 'permohonan_id': id });
        };

        vm.confirmDelete = function(id) {
            var dialog = {
                title: 'Apakah anda yakin?',
                content: 'Data yang sudah dihapus tidak dapat dikembalikan!',
                ok: 'Ya',
                cancel: 'Tidak'
            };

            $mdDialog.show(
                $mdDialog.confirm()
                .title(dialog.title)
                .textContent(dialog.content)
                .ok(dialog.ok)
                .cancel(dialog.cancel)
            ).then(function() {
                DaftarIzinFactory.deleteDaftarIzin(id).then(function() {
                    getData();
                });
            });
        };

        vm.showProcess = showProcess;

        ///////////
        function showProcess(permohonanIzinId) {
            var useFullScreen = ($mdMedia('sm') || $mdMedia('xs')) && $scope.customFullscreen;
            $mdDialog.show({
                controller: IzinProgressDialogController,
                templateUrl: 'app/daftar-izin-module/progress-dialog.tmpl.html',
                parent: angular.element($document.body),
                clickOutsideToClose: true,
                fullscreen: useFullScreen,
                locals: {
                    permohonanIzinId: permohonanIzinId
                }
            })
            .then(function() {
            }, function() {
            });

            $scope.$watch(function() {
                return $mdMedia('xs') || $mdMedia('sm');
            }, function(wantsFullScreen) {
                $scope.customFullscreen = (wantsFullScreen === true);
            });
        }

        /*** BEGIN - Advance Table ***/
        vm.query = {
            filter: '',
            limit: ElementConfig.gridRow,
            order: '-permohonanizin.id',
            page: 1
        };
        vm.filter = {
            options: {
                debounce: 500
            }
        };
        vm.getData = getData;
        vm.removeFilter = removeFilter;
        activate();

        ///////////
        function activate() {
            var bookmark;
            $scope.$watch('vm.query.filter', function(newValue, oldValue) {
                if (!oldValue) {
                    bookmark = vm.query.page;
                }

                if (!newValue) {
                    vm.query.page = bookmark;
                }

                if (newValue !== oldValue) {
                    vm.getData();
                }
            });
        }

        function getData() {
            DaftarIzinFactory.getPagedDaftarIzin(vm.query).then(function(res) {
                vm.data = res.data.data;
            });
        }

        function removeFilter() {
            vm.filter.show = false;
            vm.query.filter = '';

            if (vm.filter.form.$dirty) {
                vm.filter.form.$setPristine();
            }
        }
        /*** END - Advance Table ***/
    }
})();

function IzinProgressDialogController(
    $state, $scope, $mdDialog, $http, EnvironmentConfig, permohonanIzinId,
    MenuService, DaftarIzinFactory, FormFactory, PermohonanIzinFactory
) {
    $scope.prosesPermohonan = [];

    DaftarIzinFactory.getProsesPermohonan(permohonanIzinId).then(function(res) {
        $scope.prosesPermohonan = res.data.data;
    }, function() {
        $mdDialog.cancel();
    });

    $scope.hide = function() {
        $mdDialog.hide();
    };
    $scope.cancel = function() {
        $mdDialog.cancel();
    };

    $scope.openForm = function(tautan, formId, prosesPermohonanId) {
        $mdDialog.hide();
        MenuService.openLink(DaftarIzinFactory.mapLink(tautan), formId, permohonanIzinId, null, prosesPermohonanId);
    };

    // Download report generated by jasper
    $scope.downloadReport = function(reportId, prosesPermohonanId) {
        FormFactory.downloadReport(reportId, permohonanIzinId);
        PermohonanIzinFactory.getNextStep(permohonanIzinId, prosesPermohonanId, false);
    };
}
